# MAGIC %md ### Continuing with Python
# MAGIC
# MAGIC Congratulations for making it this far! This course just scratched the surface of what's possible with python and data analysis. To continue your journey, take a look at the following resources. [You can also check out more courses by Databricks.](https://academy.databricks.com)
# MAGIC
# MAGIC For continuing with python:
# MAGIC
# MAGIC - [Introduction to Computational Thinking and Data Science](https://ocw.mit.edu/courses/6-0002-introduction-to-computational-thinking-and-data-science-fall-2016/) is MIT's introduction programming class. [It also has an accompanying book by John Guttag](https://www.amazon.com/Introduction-Computation-Programming-Using-Python/dp/0262529629/ref=sr_1_1?crid=36AMZ24HWKRMQ1). Both are excellent introductions to python
# MAGIC - [Leet Code](https://leetcode.com/) is a great resource for practicing coding problems
# MAGIC - [Fluent Python](https://www.oreilly.com/library/view/fluent-python/9781491946237/) by Luciano Ramalho is a good introduction to python suitable for beginners
# MAGIC
# MAGIC For continuing with data analysis and data science:
# MAGIC
# MAGIC - [Python for Data Analysis](https://www.oreilly.com/library/view/python-for-data/9781449323592/) was written by Wes McKinney, the developer of `pandas`. It's a great way to deepen your understanding of scientific programming
# MAGIC - [fast.ai](https://www.fast.ai/) offers industry standard, free classes that introduce you to neural networks
# MAGIC - [Deep Learning for Coders with fastai and PyTorch](https://www.oreilly.com/library/view/deep-learning-for/9781492045519/) is written by Jeremy Howard and Sylvain Gugger, developers of `fastai`
# MAGIC - [Kaggle](https://www.kaggle.com/) is an online competition website for data scientists. Highly ranked players never have to worry about finding a job :-)
# MAGIC - [Deep Learning with Python](https://www.oreilly.com/library/view/deep-learning-with/9781617296864/) was written by Francois Chollet, the originial developer of the `keras` API for the neural network library `tensorflow`

